void main(){

  //while statement
  print("WHILE");
  var sum = 0;
  while (sum<=10) {
    print(sum);
    sum++;
  }

  //do while statement
  print("DO WHILE");
  var sum2 = 0;
  do {
    print(sum2);
    sum2++;
  } while (sum2<=10);

  //for statement
  print("FOR");
  int sum3 = 10;
  for (int i = 0; i <= sum3; i++) {
    print(i);
    
  }
}